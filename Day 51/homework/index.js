 // გამოიყენეთ ternary ოპერატორი იმის დასადგენად, არის თუ არა პაროლის სიგრძე 8 სიმბოლოზე მეტი ან ტოლი.
 // გამოიტანეთ შეტყობინება, რომელიც მიუთითებს, არის თუ არა პაროლი "საკმარისად გრძელი" თუ "ძალიან მოკლე".
//მაგალითად, თუ შეყვანილია "MySecurePassword", პროგრამამ უნდა გამოიტანოს: "პაროლი საკმარისად გრძელია". 
// ხოლო თუ შეყვანილია "Pass1", უნდა გამოიტანოს: "პაროლი ძალიან მოკლეა".

