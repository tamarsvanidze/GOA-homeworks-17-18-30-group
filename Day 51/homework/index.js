//# 5. ფასდაკლების განსაზღვრა
 //დაწერეთ პროგრამა, რომელიც იღებს მომხმარებლის მიერ შეძენილი საქონლის რაოდენობას (მთელი რიცხვი).
// გამოიყენეთ ternary ოპერატორი შემდეგი ფასდაკლების ლოგიკის დასადგენად:
//თუ რაოდენობა მეტია ან ტოლია 10-ზე, ფასდაკლება არის 10%. წინააღმდეგ შემთხვევაში, ფასდაკლება არის 0%.
//გამოიტანეთ შეტყობინება, რომელიც მიუთითებს გამოყენებულ ფასდაკლების პროცენტს.მაგალითად, თუ შეყვანილია 12,
//  პროგრამამ უნდა გამოიტანოს: "თქვენი ფასდაკლებაა: 10%". ხოლო თუ შეყვანილია 5, უნდა გამოიტანოს: "თქვენი ფასდაკლებაა: 0%".

let amount = Number(prompt("შეიყვანე საქონლის რაოდენობა:"))
let discount = (amount >= 10) ? 10 : 0

console.log("თქვენი ფასდაკლებაა: " + discount + "%")

