//# 5. FizzBuzz
//შექმენით FizzBuzz პროგრამა, რომელიც მუშაობს შემდეგნაირად
//1. თუ რიცხვი უნაშთოდ იყოფა 3-ზე, დაიბეჭდოს "Fizz".
//2. თუ რიცხვი უნაშთოდ იყოფა 5-ზე, დაიბეჭდოს "Buzz".
//3. თუ რიცხვი უნაშთოდ იყოფა ორივე (3-ზეც და 5-ზეც), დაიბეჭდოს "FizzBuzz".
//4. თუ არცერთი პირობა არ შესრულდება, დაიბეჭდოს თავად რიცხვი

for (let i = 1; i <= 100; i++) {
    if (i % 3 === 0 && i % 5 === 0) {
        console.log("FizzBuzz")
    } else if (i % 3 === 0) {
        console.log("Fizz")
    } else if (i % 5 === 0) {
        console.log("Buzz")
    } else {
        console.log(i)
    }
}
