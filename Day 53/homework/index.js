//# 2. 1-დან 10-მდე რიცხვების ჯამი
//დავალება:შექმენით for loop, რომელიც გამოთვლის 1- დან 10 -მდე 
// რიცხვების ჯამს, საბოლოო შედეგი გამოიტანეთ console - ში.
